#include "ft_printf.h"

void	ft_uputnbr_fd(unsigned int nbr, int fd)
{
	if (nbr > 9)
	{
		ft_uputnbr_fd(nbr / 10, fd);
	}
	ft_putchar_fd(((nbr % 10) + '0'), fd);
}
