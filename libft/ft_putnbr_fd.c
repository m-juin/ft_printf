#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	printer;

	printer = 0;
	if (n == INT_MIN)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			n *= -1;
		}
		if (n > 9)
			ft_putnbr_fd((n / 10), fd);
		printer = (n % 10) + 48;
		ft_putchar_fd(printer, fd);
	}
}
